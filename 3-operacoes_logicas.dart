void main(){

 // comparadores
 // -------------------------------------------------------------
 // >      maior
 // >=     maior ou igual
 // <      menor
 // <=     menor ou igual
 // ==     igual
 // !=     diferente

 double num1 = 15.0;
 bool testeComp = (10 != num1);
 print("COMP: $testeComp");

 // Operador OR
 // -------------------------------------------------------------
 bool testeOr = (false || true);
 print("OR: $testeOr");



 // Operador AND
 // -------------------------------------------------------------
 bool testeAnd = (false && true);
 print("AND: $testeAnd");

 // Operador NOT
 print(!true);

}